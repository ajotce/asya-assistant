import { useEffect } from "react";

import { applySeo } from "../../seo";

type LegalPageVariant = "privacy" | "terms" | "data-policy";

interface LegalPageProps {
  variant: LegalPageVariant;
}

export default function LegalPage({ variant }: LegalPageProps) {
  const content = getContent(variant);

  useEffect(() => {
    applySeo({
      title: content.title,
      description: content.description,
      path: content.path,
    });
  }, [content]);

  return (
    <section className="page public-page" aria-label={content.heading}>
      <h1 className="page__title">{content.heading}</h1>
      {content.paragraphs.map((paragraph) => (
        <p key={paragraph}>{paragraph}</p>
      ))}
      <h2>TODO для владельца проекта</h2>
      <ul>
        {content.todo.map((item) => (
          <li key={item}>{item}</li>
        ))}
      </ul>
    </section>
  );
}

